package notsotiny.lang.ir;

/**
 * Possible Conditions
 * 
 * @author Mechafinch
 */
public enum IRCondition {
    E,      // Equal
    NE,     // Not Equal
    A,      // Above
    AE,     // Above or Equal
    B,      // Below
    BE,     // Below or Equal
    G,      // Greater
    GE,     // Greater or Equal
    L,      // Less
    LE,     // Less or Equal
    NONE,   // Unconditional
    ;
}
